import java.util.*;

public class SolveMeFirst {
    
    public static void solveMeFirst(int a, int b) {
        System.out.println(a+b);
    }

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        sc.close();
        solveMeFirst(a, b);
    }
    
}
